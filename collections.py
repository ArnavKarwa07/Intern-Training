# Lists
fruits = ['apple', 'banana', 'orange']
print(fruits)
print(fruits[0])

# Tuples
afruits = ('apple', 'banana', 'orange')
print(afruits)
print(afruits[0])

# Sets
numbers = {1, 2, 3}
print(numbers)

#Dictionaries
name = {
    "first_name" : "Arnav",
    "last_name" : "Karwa"
}

print(name)
print(name["first_name"])